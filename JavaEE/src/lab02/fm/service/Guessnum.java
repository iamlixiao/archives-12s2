package lab02.fm.service;

public class Guessnum {
	int num, gsn, vsn;

	public Guessnum() {
		gsn = vsn = 0;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getGsn() {
		return gsn;
	}

	public void setGsn(int gsn) {
		this.gsn = gsn;
	}

	public int getVsn() {
		return vsn;
	}

	public void setVsn(int vsn) {
		this.vsn = vsn;
	}
}
